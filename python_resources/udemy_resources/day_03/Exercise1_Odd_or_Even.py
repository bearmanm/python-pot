# if else odd or even statement

number = int(input()) #21

# If the number can be divided by 2 with 0 remainder.
if number %2 ==0:
  print("Odd")
else:
  print("Even")
# = Odd
  
# The % operator is the modulo operator, which gives the remainder of the division. 
# number %2 gives the remainder when number is divided by 2. 
# If the remainder is not zero (i.e., number %2 is not equal to zero), it is evaluated as True. If the remainder is zero, it is evaluated as False.

